# Escreva um progrma que leia um valor em metros e exiba convertido em centímetro e milímetros.

metro = int(input("Digite quantos metros = "))

centimetros = metro * 100
milimetros = metro * 1000

print(" Metros = {} \n Centímetros = {} \n Milímetros = {} ".format(metro, centimetros, milimetros))

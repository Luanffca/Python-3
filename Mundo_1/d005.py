# Crie um algoritimo que leia um número e mostre o seu dobro triplo e rias qudrada.

nu = int(input("Digite um número = "))

doplo = nu * 2
triplo = nu * 3 
raiz_quadrada = nu ** (1/2)

print("O número que você digitou foi {} e seu dobro é {}, triplo {} e sua raiz quadrada {}"
.format(nu, doplo, triplo, raiz_quadrada))
